<article class="how-to">
  Having the ability to monitor your portfolio holdings online is easy to accomplish with IAM and Yahoo Finance. Simply follow these steps.<br>
  <br>

  Users will need to decide: Do I want my actual share values (lot by lot) uploaded, or simply the security names without actual values.<br>
  <br>

  Both methods provide meaningful results available for review by cell phone, tablet, etc. after installing the Yahoo! Finance free application on your device.<br>
  <br>

  <strong>To Import your Investment Account Manager portfolio, simply follow these steps:</strong><br>
  <ol>
    <li>
      Open IAM and select the portfolio from the portfolios list.<br>
      <img src="images/how-to-uploading-your-portfolio-to-yahoo-finance1.jpg" alt=""><br>
      <br>
    </li>

    <li>
      Select File Menu | Export | Yahoo Portfolio Data.<br>
      <img src="images/how-to-uploading-your-portfolio-to-yahoo-finance2.jpg" alt=""><br>
      <br>
    </li>

    <li>
      Select your preference: Tax lots (actual portfolio) or One Share of Each Asset.<br>
      <img src="images/how-to-uploading-your-portfolio-to-yahoo-finance3.jpg" alt=""><br>
      <br>
    </li>

    <li>
      Investment Account Manager will create and save the file to use with Yahoo! Finance.  Note the location of the file so you can find it.<br>
      <img src="images/how-to-uploading-your-portfolio-to-yahoo-finance4.jpg" alt=""><br>
      <br>
    </li>

    <li>When complete, close the open windows and minimize Investment Account Manager.</li>
  </ol>
  <br>
  <br>

  <strong>Steps to complete in Yahoo</strong><br>
  <ol>
    <li>Log-into Yahoo (<a href="http://www.yahoo.com">http://www.yahoo.com</a>) or download and install the Smartphone app to your device.<br><br></li>
    <li>Either sign-in or sign-up for a new account.<br><br></li>
    <li>After logging into your account, select Finance from the left panel on Yahoo's Home Page.  This will take you to Yahoo Finance home page (<a href="http://finance.yahoo.com">http://finance.yahoo.com</a>).<br><br></li>
    <li>Click on "My Portfolio" in the Menu Bar and then Create Portfolio.<br><br></li>

    <li>Look for "Import". Click on "Import".<br><br></li>

    <li>
      In the Import window click on "Choose File". Find the file, then double-click on it to select it for import to Yahoo!, and Submit button. After the page refreshes, you will have uploaded your portfolio to Yahoo!<br>
      <img src="images/how-to-uploading-your-portfolio-to-yahoo-finance4.jpg" alt=""><br>
      <br>
    </li>

    <li>The final step is to rename your portfolio on Yahoo!'s website: Click on "Settings" then Rename.</li>
  </ol>
  <br>

  <strong>That's it... you now can monitor your portfolio from anywhere in the world!</strong>
</article>

