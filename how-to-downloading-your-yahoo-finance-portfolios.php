  Since 1985 Investment Account Manager has provided investors with detailed portfolio management tools and professional reporting.<br>
  <br>

  <strong>Importing from Yahoo! Finance</strong><br>
  <ol>
    <li>
      Go to <a href="https://finance.yahoo.com">https://finance.yahoo.com</a> and then select My Portfolio. Select the portfolio you wish to export to IAM to open within Yahoo!.<br>
      <img src="images/how-to-downloading-your-yahoo-finance-portfolios1.jpg" alt=""><br>
      <br>
    </li>

    <li>
      Next click on Export. This will create a file called "quotes.csv" which will be saved to your PC's download folder.<br>
      <img src="images/how-to-downloading-your-yahoo-finance-portfolios2.jpg" alt=""><br>
      <br>
    </li>
  </ol>
  <br>

  <strong>Creating the portfolio in Investment Account Manager</strong><br>
  <ol>
    <li>
      Start IAM and selected File Menu | Import | Yahoo Portfolio Data.<br>
      <img src="images/how-to-downloading-your-yahoo-finance-portfolios3.jpg" alt=""><br>
      <br>
    </li>

    <li>
      Now give the portfolio a name within Investment Account Manager, and then click on Next.<br>
      <img src="images/how-to-downloading-your-yahoo-finance-portfolios4.jpg" alt=""><br>
      <br>
    </li>

    <li>
      Select the location of the file downloaded from Yahoo!, then the file, and then the Select button and then click on import.<br>
      <img src="images/how-to-downloading-your-yahoo-finance-portfolios5.jpg" alt=""><br>
      <img src="images/how-to-downloading-your-yahoo-finance-portfolios6.jpg" alt=""><br>
      <br>
    </li>

    <li>
      Check the following screens closely for any editing which may be needed.<br>
      <img src="images/how-to-downloading-your-yahoo-finance-portfolios7.jpg" alt=""><br>
      <img src="images/how-to-downloading-your-yahoo-finance-portfolios8.jpg" alt=""><br>
      <br>
    </li>

    <li>
      Review IAM's import audit report.<br>
      <img src="images/how-to-downloading-your-yahoo-finance-portfolios9.jpg" alt=""><br>
      <br>
    </li>
  </ol>
  <br>

  Your portfolio is now part of Investment Account Manager, as shown on IAM's Home Page.<br>
